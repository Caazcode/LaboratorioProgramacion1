    private void mostrarCanciones() {
        
    panelCanciones.removeAll();
    panelCanciones.setLayout(new java.awt.GridLayout(0, 1)); 

        
    Song[] canciones = biblioteca.getCanciones(); 
    int cantidad = 0;

    for (int i = 0; i < canciones.length && cantidad < 4; i++) {
        Song s = canciones[i];

        if (s != null) {
            ImageIcon icono = s.getImagenDisco(); 
            if (icono != null) {
                Image imagen = icono.getImage().getScaledInstance(100, 100, Image.SCALE_SMOOTH);
                ImageIcon imagenEscalada = new ImageIcon(imagen);

                javax.swing.JPanel panelCancion = new javax.swing.JPanel();
                panelCancion.setBackground(new java.awt.Color(153, 255, 255));
                panelCancion.setLayout(new java.awt.FlowLayout());

                
                JLabel imagenLabel = new JLabel(new ImageIcon(icono.getImage().getScaledInstance(80, 80, Image.SCALE_SMOOTH)));

                
                JTextArea texto = new JTextArea();
                texto.setText("Nombre: " + s.getNombre() +
                              "\nCÃ³digo: " + s.getCodigo() +
                              "\nPrecio: $" + s.getPrecio() +
                              "\nRating: " + String.format("%.1f", s.songRating()) + " estrellas");
                texto.setEditable(false);
                texto.setOpaque(false);
                texto.setFont(new java.awt.Font("Arial", 0, 12));

                
                panelCancion.add(imagenLabel);
                panelCancion.add(texto);

                
                panelCanciones.add(panelCancion);


                
                cantidad++;
            }
                    }
                }

                panelCanciones.revalidate();
                panelCanciones.repaint();
                    }


    
    
    
